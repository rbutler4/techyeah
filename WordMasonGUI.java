class countdownThread extends Thread {
	int timer = 0;
	WordMasonGUI parent;
	
	public countdownThread(int timer, WordMasonGUI parent){
		this.timer = timer;
		this.parent = parent;
	}
	
	public void run() {
		for(int i = timer; i >= 0; i--) {
			parent.setNextBankTimer(i);
			try {
				Thread.sleep(1000);
			} catch (InterruptedException err) {
				break;
			}			
		}
	}
}
